package sitemasterData;

public class SitemasterData {
	
	
	int id; String siteId; String sitename; String fscname; String circle; String zone; String cluster;String district;





	public SitemasterData(int id, String siteId, String sitename, String fscname, String circle, String zone,
			String cluster, String district) {
		super();
		this.id = id;
		this.siteId = siteId;
		this.sitename = sitename;
		this.fscname = fscname;
		this.circle = circle;
		this.zone = zone;
		this.cluster = cluster;
		this.district = district;
	}



	

}
